    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here: BOTAO CALCULAR

        //Criando um novo objeto referente a uma classe
        Exercicio18_CLASSE obj = new Exercicio18_CLASSE(0,"0",0,0,0);
        
        //Tratamentos de erros
        try{            
            //Se a caixa de texto estiver vazia, iremos exibir um erro
            if (txtAngulo.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Erro! Digite um valor para o angulo!");
            return;
        }else{
            //Se o valor inserido na caixa de texto conter uma virgula, iremos substituir essa virgula por um ponto
            if (txtAngulo.getText().contains(",")){
                obj.setAnguloStr(txtAngulo.getText());
                obj.corrigirVirgula();
            }else{
                //Adicionando a um Setter, o valor convertido de uma caixa de texto
                obj.setAngulo(Double.parseDouble(txtAngulo.getText()));            
            }            
        }            
        //Caso o usuario tenha digitado qualquer valor alem de numeros, iremos exibir um erro
        }catch(NumberFormatException e ){
            JOptionPane.showMessageDialog(null, "Erro! Digite somente valores numericos!");
            return;            
        }
        
        //Chamando dos metodos criados na classe
        obj.calcularSeno();
        obj.calcularCosseno();
        obj.calcularTangente();
        
        //Imprimindo resultados em caixas de texto
        txtSin.setText(String.valueOf(obj.getSeno()));
        txtCos.setText(String.valueOf(obj.getCosseno()));
        txtTan.setText(String.valueOf(obj.getTangente()));
        
    }//GEN-LAST:event_btnCalcularActionPerformed

