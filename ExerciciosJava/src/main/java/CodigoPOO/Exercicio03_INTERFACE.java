    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here: BOTAO CALCULAR
        
        //Criado novo objeto
        Exercicio03_CLASSE obj = new Exercicio03_CLASSE();        
        
        //Chamando dos setters criados na classe        
        obj.setV1(Integer.parseInt(txtV1.getText()));
        obj.setV2(Integer.parseInt(txtV2.getText()));
        
        //Chamando do metodo criado na classe
        obj.calcularSoma();
        
        //Atribuindo a caixa de texto resultado, o atributo criado dentro do metodo acima
        txtResultado.setText(String.valueOf(obj.getResultado()));
        
    }//GEN-LAST:event_btnCalcularActionPerformed

