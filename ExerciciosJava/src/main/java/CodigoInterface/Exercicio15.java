    private void sliDiasAlugadosStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sliDiasAlugadosStateChanged
        // TODO add your handling code here: SLIDER DIAS ALUGADOS
        txtDiasAlugados.setText(String.valueOf(sliDiasAlugados.getValue()));
        
        //Inserindo em uma variavel integer a quantidade de dias alugados inserido em uma caixa de texto
        int diasAlugados = Integer.parseInt(txtDiasAlugados.getText());
        
        //Calculando o valor a pagar baseado na quantidade de dias alugados (R$60 por dia alugado)
        diasAPagar = (diasAlugados*60);
        
        //Inserindo a uma caixa de texto o valor a pagar de acordo com a formula matematica acima
        txtDiasAPagar.setText(String.format("%.2f", diasAPagar));
        
    }//GEN-LAST:event_sliDiasAlugadosStateChanged

    private void btnCalcularTotalAPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularTotalAPagarActionPerformed
        // TODO add your handling code here: BOTAO CALCULAR
        
        double valorTotal = (diasAPagar+kmAPagar);
        txtTotalAPagar.setText(String.format("%.2f", valorTotal));
        
    }//GEN-LAST:event_btnCalcularTotalAPagarActionPerformed

    private void btnCalcularKmAPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularKmAPagarActionPerformed
        // TODO add your handling code here: BOTAO CALCULAR VALOR A PAGAR POR KM
        
        //Inserindo a uma variavel integer a quantidade de KMs percorridos inseridos em uma caixa de texto
       double kmPercorrido = Double.parseDouble(txtKm.getText());
       
       //Calculando o valor a pagar de acordo com as informações acima
       kmAPagar = (kmPercorrido*0.15);
       
       //Inserindo o valor a pagar de acordo com a formula matematica acima em uma caixa de texto
       txtKmAPagar.setText(String.format("%.2f", kmAPagar));
        
    }//GEN-LAST:event_btnCalcularKmAPagarActionPerformed

