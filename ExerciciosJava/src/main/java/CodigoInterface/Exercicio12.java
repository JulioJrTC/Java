    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here: BOTAO CALCULAR      

        //Variavel para o preco atual digitado em uma caixa de texto
        double precoAtual;

        //Mecanica de erro
        try {
            //Inserindo a uma variavel double o valor inserido em uma caixa de texto
            precoAtual = Double.parseDouble(txtPrecoAtual.getText());
        } catch (NumberFormatException ERRO) {
            JOptionPane.showMessageDialog(null, "Erro! Por favor digite um valor monetario (usando ponto como separador)");
            return;
        }

        //Iremos atribuir a uma variavel double o valor e o desconto (de acordo com o valor do slider)
        double precoNovo = precoAtual - (precoAtual * sliDesconto.getValue() / 100);

        //Imprimindo novo valor em uma caixa de texto
        txtPrecoNovo.setText(String.format("%.2f", precoNovo));

    }//GEN-LAST:event_btnCalcularActionPerformed

