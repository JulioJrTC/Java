    //Metodo que irá conter a logica de reprodução e leitura de midia MP3
    public void playMusic(){
        
        //Para evitar que seja possivel tocar a musica diversas vezes uma por cima da outra, iremos verificar 
        //se a mesma já não está sendo reproduzida e iremos separar esse processo em uma Thread, 
        //para que o programa não congele durante a reprodução da musica
        
        if(musicaThread == null || !musicaThread.isAlive()){
            
            //Criando uma Thread para o codigo identado
            musicaThread = new Thread(() -> {            
                try{
                FileInputStream lerArquivo = new FileInputStream(txtCaminhoMusica.getText());
                musicaPlayer = new Player(lerArquivo);            
                musicaPlayer.play();            
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "Erro! Não foi possivel ler o arquivo!, erro: "+ex);            
            }
            });
        musicaThread.start();
        }
    }  
    
    //Metodo que irá conter a logica de encerramento da musica
    public void stopMusic(){
        if (musicaPlayer !=null){
            musicaPlayer.close();
        }
    }
    
