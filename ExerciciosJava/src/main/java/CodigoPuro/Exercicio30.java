package CodigoPuro;

public class Exercicio30 {
    public static void main(String[] args){
        //Definindo uma variavel int
        int numero;
        
        //A variavel abaixo irá receber um numero qualquer
        numero=3;
        
        //Identificando se o numero referente a variavel acima é PAR ou IMPAR
        if (numero%2==0){
            System.out.printf("O numero '%d' é PAR!",numero);
        }
        else{
            System.out.printf("O numero '%d' é IMPAR!",numero);
        }
        
    }    
}
