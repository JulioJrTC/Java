package CodigoPuro;

public class Exercicio05 {
    
    public static void main(String[] args){
        //Definindo variaveis integer        
        int numero,numeroAnt,numeroSup;
        
        //A variavel do numero escolhido irá receber 50
        numero=50;
        
        //A variavel do numero antecessor irá receber a variavel anterior - 1
        numeroAnt=numero-1;
        
        //A variavel do numero antecessor irá receber a variavel numero +1
        numeroSup=numero+1;
    
        //Imprimindo os valores
        System.out.println("Numero: "+numero);
        System.out.println("Numero Antecessor: "+numeroAnt);
        System.out.println("Numero superior: "+numeroSup);
    }
    
}
